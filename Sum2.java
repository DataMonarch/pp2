public class Sum2 {
    public static void main(String[] args){
        float a = 1.0f/5.0f;
        float b = 2.0f/3.0f;
        float c = 3.0f/10.0f;
        float sum = a * b - c;

        System.out.printf(a + " * " + b + " - " + c + " = " + sum);
    }
    
}


